/**
 * The orientation of an axis label relative to the axis.
 */
public enum LabelOrientation {
    
    /** Labels are drawn perpendicular to the axis line. */
    PERPENDICULAR,
    
    /** Labels are drawn parallel to the axis line. */
    PARALLEL
    
}
